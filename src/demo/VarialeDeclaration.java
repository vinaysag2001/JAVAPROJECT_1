package demo;

public class VarialeDeclaration {
    public static void main(String[] args) {
        //syntax : declaration Datatype varName;
        String animeName;
        animeName="NARUTO";
        System.out.println("ANIME NAME:"+animeName);
        // changing the value of variable
        animeName="DEMON SLAYER";
        System.out.println("the value of variable after changing ANIME NAME:"+animeName);

        int age;
        age=17;
        System.out.println("AGE:"+age);
        age=19;
        System.out.println("the value of variable after changing AGE:"+age);

        double salary;
        salary=45845.58;
        System.out.println("salary:"+salary);
        salary=494444884.5;
        System.out.println("the value of variable after changing SALARY:"+salary);

    }
}

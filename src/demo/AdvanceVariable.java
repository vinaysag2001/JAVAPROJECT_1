package demo;

public class AdvanceVariable {
    public static void main(String[] args) {
        int a = 120, b = 20;
        int sum, sub, multi, div, percent;
        sum = a + b;
        sub = a - b;
        multi = a * b;
        div = a / b;
        percent = a * b / 100;
        System.out.println("addition " + sum + " subtraction " + sub + "multiplication " + multi + "division " + div);
        System.out.println("percentage " + percent);
    }}
